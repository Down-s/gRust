gRust.ItemRegister("apple")
:SetName("Apple")
:SetDescription("The apple is a food that gives the player food and water. Unlike most other foods the apple gives lots more hydration when consumed. Can be found in food crates and medical crates.")
:SetStack(10)
:SetIcon("materials/items/consumable/apple.png")
:SetMaterial("Wood")
:SetCalories(30)
:SetHydration(15)
:SetInstantHealth(2)
:Register()

