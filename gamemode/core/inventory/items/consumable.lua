gRust.ItemRegister("pickles")
:SetName("Pickles")
:SetDescription("Pickles are edible food. However, you have a chance of getting your hunger and hydration decreased (you'll hear a vomit sound). It is recommended you save up pickles to feed horses, use as a bait for fish traps or compost for fertilizers.")
:SetStack(10)
:SetIcon("materials/items/consumable/pickles.png")
:SetMaterial("Wood")
:SetCalories(50)
:SetHydration(20)
:SetInstantHealth(5)
:SetOnConsumed(function(pl, inventory, index)
    if (math.random(1, 3) == 1) then
        pl:EmitSound(string.format("player/vomit_%i.wav", math.random(1, 2)))
        pl:SetHealth(pl:Health() - 5)
        pl:SetCalories(pl:GetCalories() - 80)
        pl:SetHydration(pl:GetHydration() - 40)

        return true
    end
end)
:Register()